public class Exercicio66C {
	    public static void main(String[] args) {
	        int soma = 0;

	        for (int contador = 0; contador <= 100; contador++) {
	            soma += contador;
	        }

	        System.out.println("A soma de 1 até 100 é: " + soma);
	    }
	}
